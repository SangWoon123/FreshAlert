-- Manager 테이블 생성
CREATE TABLE manager (
    id INT NOT NULL AUTO_INCREMENT PRIMARY KEY,
    code INT UNIQUE NOT NULL  -- Integer 타입으로 변경
);

-- ProductName 테이블 생성
CREATE TABLE product_name (
    id INT NOT NULL AUTO_INCREMENT PRIMARY KEY,
    name VARCHAR(128) NOT NULL UNIQUE  -- name은 VARCHAR(128) 타입으로 설정
);

-- Product 테이블 생성
CREATE TABLE product (
    id INT NOT NULL AUTO_INCREMENT PRIMARY KEY,
    product_name_id INT NOT NULL,
    quantity INT,
    expiration DATE,  -- 날짜 형식
    checked BOOLEAN DEFAULT FALSE,  -- Boolean 필드, 기본값은 FALSE
    created_at DATETIME DEFAULT CURRENT_TIMESTAMP,  -- 생성 날짜, 기본값은 현재 시간
    deleted_at DATETIME NULL,  -- 삭제 날짜, NULL 허용
    CONSTRAINT fk_product_name FOREIGN KEY (product_name_id) REFERENCES product_name(id)  -- 외래키 설정
);

