INSERT INTO product (`product_name_id`, `expiration`, `quantity`, `checked`, `created_at`, `deleted_at`)
WITH RECURSIVE cte (n) AS
(
  SELECT 1
  UNION ALL
  SELECT n + 1 FROM cte WHERE n < 3000000 -- 원하는 데이터 개수
)
SELECT 
    (SELECT id FROM product_name ORDER BY RAND() LIMIT 1) AS product_name_id, -- product_name 테이블에서 랜덤 id 선택
    DATE_ADD('2024-01-01', INTERVAL FLOOR(RAND() * 365) DAY) AS expiration,      -- 최대 1년 이후의 랜덤 유통기한
    FLOOR(1 + RAND() * 100) AS quantity,                                      -- 1부터 100 사이의 랜덤 수량
    FALSE AS checked,                                                         -- 체크 여부 false로 설정
    NOW() AS created_at,                                                      -- 현재 시간
    NULL AS deleted_at                                                        -- 삭제 시간은 NULL
FROM cte
LIMIT 3000000;



